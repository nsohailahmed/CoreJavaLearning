package com.thecodecraving.basics.operators;

public class RelationalOperators {
	
	public static void main(String[] args) {
		int a =10, b=20, c=10;
		
		System.out.println("Equals Relational Operator "+(a==c));
		System.out.println("Not Equal Relational Operator "+(a!=c));
		System.out.println("> Relational Operator "+(a>b));
		System.out.println("< Relational Operator "+(a<b));
		System.out.println(">= Relational Operator "+(a<=c));
		System.out.println("<= Relational Operator "+(a>=c));
	}

}
