package com.thecodecraving.basics.operators;

public class UnaryOperators {
	
	public static void main(String[] args) {
		int a =5, b =10;
	
		System.out.println("Unary Plus --> "+(+a));
		System.out.println("Unary Minus --> "+(-a));
		System.out.println("Increment Operator --> "+(++a));
		System.out.println("Decrement Operator --> "+(--b));
		System.out.println("Logical not --> "+!(a<b));
	}

}
